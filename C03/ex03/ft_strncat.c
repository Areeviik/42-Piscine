char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char *p1;
	char *p2;

	p1 = dest;
	p2 = src;
	while (*p1)
	{
		p1++;
	}
	while (nb > 0 && *p2)
	{
		*p1 = *p2;
		p1++;
		p2++;
		nb--;
	}
	*p1 = 0;
	return (dest);
}
