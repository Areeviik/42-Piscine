int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s2[i] == s1[i])
	{
		if (s1[i] == '\0')
		{
			return (0);
		}
		i++;
	}
	return ((int)s1[i] - (int)s2[i]);
}
